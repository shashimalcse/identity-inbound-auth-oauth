package org.wso2.carbon.identity.openidconnect;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.wso2.carbon.identity.oauth2.IdentityOAuth2Exception;
import org.wso2.carbon.identity.oauth2.authz.OAuthAuthzReqMessageContext;
import org.wso2.carbon.identity.oauth2.dto.OAuth2AccessTokenRespDTO;
import org.wso2.carbon.identity.oauth2.dto.OAuth2AuthorizeRespDTO;
import org.wso2.carbon.identity.oauth2.token.OAuthTokenReqMessageContext;

import java.util.HashMap;
import java.util.Map;

/**
 * This class is used to inject agents claims.
 */
public class OIDCAgentClaimProviderImpl implements ClaimProvider {

    public static final Log LOG = LogFactory.getLog(OIDCAgentClaimProviderImpl.class);
    private static final String AGENT = "AGENT";
    private static final String AUT = "aut";

    @Override
    public Map<String, Object> getAdditionalClaims(OAuthAuthzReqMessageContext authAuthzReqMessageContext,
                                                   OAuth2AuthorizeRespDTO authorizeRespDTO)
            throws IdentityOAuth2Exception {

        return null;
    }

    @Override
    public Map<String, Object> getAdditionalClaims(OAuthTokenReqMessageContext tokenReqMessageContext,
                                                   OAuth2AccessTokenRespDTO tokenRespDTO)
            throws IdentityOAuth2Exception {

        if (AGENT.equalsIgnoreCase(tokenReqMessageContext.getAuthorizedUser().getUserStoreDomain())) {
            Map<String, Object> agentMap = new HashMap<>();
            agentMap.put(AUT, AGENT);
            return agentMap;
        }
        return null;
    }
}
